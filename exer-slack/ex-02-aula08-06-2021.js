function estacoes(estacao) {
    switch (estacao) {
        case estacao == "Verão" : console.log("Que calor hein?!");
        break
        case estacao == "Inverno" : console.log("Frio dms!");
        break
        case estacao == "Outono" : console.log("É a estação em que as folhas caem.");
        break
        case estacao == "Primavera" : console.log("O momento em que as folhas crescem.");
        default : console.log("Ops, estação inválida!")
    }
}